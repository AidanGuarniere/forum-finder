// requirements
const { Model, DataTypes } = require("sequelize");
const sequelize = require("../config/connection");

// create Forum model
class Forum extends Model {}

// define Forum columns
Forum.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    title: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    initial_message: {
      type: DataTypes.TEXT,
      allowNull: false,
    },
    user_id: {
      type: DataTypes.INTEGER,
      references: {
        model: "user",
        key: "id",
      },
    },
  },
  {
    sequelize,
    freezeTableName: true,
    underscored: true,
    modelName: "forum",
  }
);

module.exports = Forum;
